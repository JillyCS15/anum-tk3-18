function res = count_newton(start, to)
  f = @(x) 97.02 + 2.219*(x-1960) + 0.03045*(x-1960)*(x-1970) - 0.000413*(x-1960)*(x-1970)*(x-1980) - 0.000025583*(x-1960)*(x-1970)*(x-1980)*(x-1990) + 0.00000202167*(x-1960)*(x-1970)*(x-1980)*(x-1990)*(x-2000) - 0.000000076625*(x-1960)*(x-1970)*(x-1980)*(x-1990)*(x-2000)*(x-2010);
  
  for i=start:to
    res(i-1959) = f(i);
  endfor
  
endfunction